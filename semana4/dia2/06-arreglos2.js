/**
 * Propiedades de los arreglos
 */

// Iniciando un arreglo vacío
let nombres = [];

/**
 * arreglo.push(elemento)
 * agrega un elemento al final de los elementos (ultima posición) de un arreglo
 */

nombres.push("Jorgito");
console.log(nombres);

nombres.push("Estrellita");
console.log(nombres);

nombres.push(5);
console.log(nombres);
